package java_basics;

public class ConditionalStatements {
	public static void main(String[] args) {
		int number1 = 9;
		int number2 = 19;
		if (number1 < number2) {
			System.out.println("Less");
		} else if (number1 > number2) {
			System.out.println("Greater");
		} else {
			System.out.println("Equal");
		}
	}
}
